import { z } from "zod";
import { createTRPCRouter, protectedProcedure } from "../trpc";
import { db, schema } from "~/server/db";
import { eq } from "drizzle-orm";
import dayjs from "dayjs";

export const iofilesRouter = createTRPCRouter({
    generate: protectedProcedure.input(z.object({
        channelNumber: z.number()
    })).mutation(async ({ ctx, input }) => {
        const transactions = await db.query.payments.findMany({
            where: eq(schema.payments.product_number, input.channelNumber)
        })

        let text = ''

        for (const transaction of transactions) {

            const date = dayjs(transaction.first_due_date)

            const year = date.year()
            const monthName = date.format('MMMM').toUpperCase()
            const dateYYYYMMDD = date.format('YYYYMMDD')
            

            text += `411002513202303292023033000170356730103179945MEPLIFE   ARS0Com.Rur.CuotBITCOM SRL                          20\n`
            text += `421002513  ${transaction.fiscal_id_number}           2850600140001011366187000000000085000      ${monthName} ${year}            ${dateYYYYMMDD}  000000000133400\n`
            text += `422002513  ${transaction.fiscal_id_number}           ${transaction.name}\n`
        }

        return text
    })
})

